/**
 * Helper: get cities visible at a given D3 zoom scale level.
 * @param {number} scale  Current D3 zoom transform k value
 * @returns {Array}       Filtered city array
 */
export function getCitiesForZoom(scale) {
  if (scale >= 3.5) return US_CITIES                                              // all cities
  if (scale >= 2.0) return US_CITIES.filter(c => c.tier !== 'tier2')             // capitals + tier1
  return US_CITIES.filter(c => c.tier === 'capital')                             // capitals only
}
